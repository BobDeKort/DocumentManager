import UIKit

class ImagesTableViewController: UITableViewController {
    
    var collection: Collection?
    var imageUrls: [URL]?

    override func viewDidLoad() {
        super.viewDidLoad()
        if let collection = collection {
            if imageUrls == nil {
                getImageUrls()
            } else {
                tableView.reloadData()
            }
        }
    }
    
    func getImageUrls() {
        if let collection = collection {
            let urls = collection.getContentsOfZippedDirectory()
            imageUrls = urls
            tableView.reloadData()
        }
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }

    // MARK: - Table view data source

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        if let urls = imageUrls {
            return urls.count
        }
        return 0
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "ImageCell", for: indexPath) as? ImagesTableViewCell
        if let urls = imageUrls {
            cell?.imageurl = urls[indexPath.row]
        }
        
        return cell!
    }

